/**
 *
 * @param {number} a
 * @param {number} b
 */
export function sum(a, b) {
    return a + b;
}

/**
 *
 * @param {number} a
 * @param {number} b
 */
export function subtract(a, b) {
    return a - b;
}

/**
 *
 * @param {number} a
 * @param {number} b
 */
export function multiply(a, b) {
    return a * b;
}

/**
 *
 * @param {number} a
 * @param {number} b
 */
export function divide(a, b) {
    return a / b;
}
